; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; R0 -	ASCII value of character need to be printed, character counter
; R1 -	ASCII value of current character, pixel value, temp
; R2 -	memory location
; R3 -	width counter
; R4 -	height counter
; R5 -	row value
; R6 -	mask, temp


		.ORIG	x3000
		LD	R2,CHAR			; load string start location
		ADD	R2,R2,#-1
		AND	R3,R3,#0
		ADD	R3,R3,#-1		; set R3 to #-1
		
LOAD		ADD	R2,R2,#1		; increment location
		ST	R2,LOAD_LOC
		ADD	R3,R3,#1		; increment width counter
		LDI	R1,LOAD_LOC		; load character
		BRnp	LOAD			; if not null, load next character
		
		ST	R3,CHAR_NUM		; store character number to memory
		
		LD	R4,SIXTEEN		; set R4 to #16
		
NEXT_ROW	LD	R2,CHAR			; load string start location
		
		LD	R0,CHAR_NUM		; set R0 to character number
		
NEXT_CHAR	ST	R2,LOAD_LOC		; store next character location to memory
		
		LDI	R1,LOAD_LOC		; load character
		
		ADD	R1,R1,R1
		ADD	R1,R1,R1
		ADD	R1,R1,R1
		ADD	R1,R1,R1		; R1 x #16			

		LEA	R2,FONT_DATA		; load FONT_DATA start memory location
		ADD	R2,R2,R1		; character start memory location
		
		LD	R6,SIXTEEN		; set R6 to #16
		NOT	R1,R4			
		ADD	R1,R1,#1		; R1 = NOT(R4)
		ADD	R6,R6,R1		; set R6 to current line number
		ADD	R2,R2,R6		; R2 = R2 + R6
		ST	R2,CHAR_ROW		; store row location to memory

    		AND	R3,R3,#0
		ADD	R3,R3,#8    		; set R3 to #8

		LDI	R5,CHAR_ROW		; load a row of a character's font map
		LD	R6,MASK			; load MASK into R6

PIXEL_CALC	AND	R1,R5,R6		; calculate pixel value

		ADD	R5,R5,R5		; move next pixel to msb

		ST	R0,TEMP			; store char counter to a temp location

		AND	R1,R1,R1		; update CC
		BRnp	OUT_1			; if bit is 1, skip
		LDI	R0,ZERO
		BRnzp	DISPLAY			; if bit is 0, skip
OUT_1		LDI	R0,ONE
DISPLAY		OUT				; print pixel

		LD	R0,TEMP			; load char counter back

		ADD	R3,R3,#-1		; decrement width counter
		BRp	PIXEL_CALC

		LD	R2,LOAD_LOC
		ADD	R2,R2,#1		; increment memory location
		ADD	R0,R0,#-1		; decrement character counter		
		BRp	NEXT_CHAR

		ST      R0,TEMP                 ; store char counter to a temp location
		LD	R0,NEW_LINE
		OUT				; print new line
		LD      R0,TEMP                 ; load char counter back

		ADD	R4,R4,#-1		; decrement height counter
		BRp	NEXT_ROW

		HALT


ZERO		.FILL x5000
ONE		.FILL x5001
CHAR		.FILL x5002

MASK		.FILL x8000
SIXTEEN		.FILL #16
NEW_LINE	.FILL x0A

CHAR_ROW	.BLKW 1
WIDTH		.BLKW 1
LOAD_LOC	.BLKW 1
CHAR_NUM	.BLKW 1
TEMP		.BLKW 1


